package Model;

/**
 *
 * @author DELL
 */
public class Giaidua {
    public int id;
    public String ten;
    public int nam;
    public String ghichu;

    public Giaidua() {
    }

    public Giaidua(int id, String ten, int nam, String ghichu) {
        this.id = id;
        this.ten = ten;
        this.nam = nam;
        this.ghichu = ghichu;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getTen() {
        return ten;
    }

    public void setTen(String ten) {
        this.ten = ten;
    }

    public int getNam() {
        return nam;
    }

    public void setNam(int nam) {
        this.nam = nam;
    }

    public String getGhichu() {
        return ghichu;
    }

    public void setGhichu(String ghichu) {
        this.ghichu = ghichu;
    }
    
    
    
}
